#include <stddef.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*dest;

	i = 0;
	dest = s;
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
}
