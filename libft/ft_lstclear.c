#include "libft.h"
#include <stdlib.h>
#include <stddef.h>

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tmp;

	while (*lst)
	{
		tmp = (*lst)->next;
		del(lst);
		free(lst);
		lst = &tmp;
	}
	*lst = NULL;
}
